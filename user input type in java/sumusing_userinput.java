import java.util.*;
public class sumusing_userinput {
    public static void main(String[] args) {
        int a,b;
        System.out.println("enter the value of a and b");
        Scanner sc=new Scanner(System.in);
        a=sc.nextInt();
        b=sc.nextInt();
        System.out.print(a);
        System.out.println(b);
        System.out.println(a+b);

    }
}
